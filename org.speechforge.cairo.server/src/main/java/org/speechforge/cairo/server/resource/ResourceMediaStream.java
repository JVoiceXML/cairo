package org.speechforge.cairo.server.resource;

import java.io.Serializable;

/**
 * Encapsulates the parameters required for establishing the media stream between MRCPv2 clients and MRCPv2 resources.
 *
 * @author Niels Godfredsen {@literal <}<a href="mailto:ngodfredsen@users.sourceforge.net">ngodfredsen@users.sourceforge.net</a>{@literal >}
 */
public class ResourceMediaStream implements Serializable {

    private String host = null;
    private int _port = -1;

    /**
     * @return Returns the host.
     */
    public String getHost() {
        return host;
    }

    /**
     * @param host The host to set.
     */
    public void setHost(String host) {
        this.host = host;
    }

    /**
     * @return Returns the port.
     */
    public int getPort() {
        return _port;
    }

    /**
     * @param port The port to set.
     */
    public void setPort(int port) {
        _port = port;
    }

}
